package com.mycompany.swingquest_ead_cw.model;

import com.fasterxml.jackson.annotation.JsonProperty;

/**
 *
 * @author Lihindu Perera
 */
public class QuestionModel {
     @JsonProperty("questionId")
    private int questionId;

    @JsonProperty("question")
    private String question;

    @JsonProperty("answer1")
    private String answer1;

    @JsonProperty("answer2")
    private String answer2;

    @JsonProperty("answer3")
    private String answer3;

    @JsonProperty("answer4")
    private String answer4;

    @JsonProperty("correctAnswer")
    private int correctAnswer;

    // Constructor
    public QuestionModel() {}

    public QuestionModel(int questionId, String question, String answer1, String answer2, String answer3, String answer4, int correctAnswer) {
        this.questionId = questionId;
        this.question = question;
        this.answer1 = answer1;
        this.answer2 = answer2;
        this.answer3 = answer3;
        this.answer4 = answer4;
        this.correctAnswer = correctAnswer;
    }

    // Getters and Setters
    public int getQuestionId() {
        return questionId;
    }

    public void setQuestionId(int questionId) {
        this.questionId = questionId;
    }

    public String getQuestion() {
        return question;
    }

    public void setQuestion(String question) {
        this.question = question;
    }

    public String getAnswer1() {
        return answer1;
    }

    public void setAnswer1(String answer1) {
        this.answer1 = answer1;
    }

    public String getAnswer2() {
        return answer2;
    }

    public void setAnswer2(String answer2) {
        this.answer2 = answer2;
    }

    public String getAnswer3() {
        return answer3;
    }

    public void setAnswer3(String answer3) {
        this.answer3 = answer3;
    }

    public String getAnswer4() {
        return answer4;
    }

    public void setAnswer4(String answer4) {
        this.answer4 = answer4;
    }

    public int getCorrectAnswer() {
        return correctAnswer;
    }

    public void setCorrectAnswer(int correctAnswer) {
        this.correctAnswer = correctAnswer;
    }

    @Override
    public String toString() {
        return "Question{" +
                "questionId=" + questionId +
                ", question='" + question + '\'' +
                ", answer1='" + answer1 + '\'' +
                ", answer2='" + answer2 + '\'' +
                ", answer3='" + answer3 + '\'' +
                ", answer4='" + answer4 + '\'' +
                ", correctAnswer=" + correctAnswer +
                '}';
    }
}
